public class QuizIf {
    public static void main(String[] a) {
        int monat = 12;

        if (monat == 12) {
            System.out.println("12");
        } else if (monat / 2 == 6) {
            System.out.println("6");
        } else {
            System.out.println("2");
        }
    }
}
